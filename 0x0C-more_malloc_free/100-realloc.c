/**
**_realloc - reallocates a memory block using malloc and free
*@ptr: pointer to a prev memory allocated
*@old_size: viejo espacio en memoria
*@new_size: nuevo espacio de memoria
*Return: a pointer
*/

void *_realloc(void *ptr, unsigned int old_size, unsigned int new_size)
{
	ptr = malloc(old_size);
}


